  <p>The Villa de Suenos Project consisted of complete rehabilitation of both exteriors and interiors of six two story apartment buildings on two different sites located in Portland on N.E. Killingsworth. Construction started in late summer 2010 and was completed in the spring of 2011.</p>
 
 <p>Exterior work included demolition and replacement of roofing, siding, sheathing, entry doors, windows, wall insulation, entry stairs as well as all front and  rear decks. New entry stairs and decks were constructed with rot resistant pressure treated posts and beams with Trex composite deck surfaces and concrete stair treads. New powder coated steel stair and deck railings were installed at all twenty eight units. Extensive dry rot repairs were also required due to moisture intrusion in numerous locations on all buildings. New Hardi “Color Plus” pre-painted siding and trim was installed. Existing landscaping was upgraded, compacted rock walking paths were installed as well as new playground area synthetic turf.</p>
 
 <p>Interior work included removal and replacement of all flooring, cabinets and exhaust fans as well as remediation of all mold that was encountered. All units received new whole house ventilation systems to provide continuous fresh air to each apartment to help prevent future growth of mold. New work items included drywall repairs, full painting of walls and ceilings, new carpet and marmoleum flooring, cabinets, countertops, kitchen sinks and faucets, selective replacement of interior doors and millwork and repair or replacement of ventilation ductwork.</p>
 
 <p>Project also included construction of a new Laundry Center at the East site to provide tenants access to on site laundry facilities.
</p>
                 
