<p>The Villa West Project was an interior and exterior rehabilitation of a forty-eight unit affordable housing project in McMinnville, Oregon. The project also included the construction of a new office / community center building onsite. Construction commenced in June 2011 and was complete in the fall of 2011.</p>

<p>The interior scope of work included replacement of all kitchen and bath cabinetry, countertops, sinks and faucets. New ducted range hoods and fresh air vents were also installed in each unit along with selected water heaters and shower units.  Existing base board heaters were replaced with new in-wall electric heaters as part of the interior rehabilitation.  Three units were also upgraded to be fully ADA accessible, which included installation of new shower units, flooring, interior / exterior doors and ADA compliant fixtures. Additionally, improvements to the property’s existing laundry facility were completed by LMC.</p>

<p>The exterior rehabilitation included replacement of existing siding with new pre-finished fiber cement siding, installation of new vinyl windows, new composition roofing and new gutters & downspouts.  Two- foot roof extensions were installed at the gable ends and eaves of each building along with the full replacement of roof sheathing.  Each unit’s entry door and storage door was replaced as part of the rehabilitation. Site improvements consisted of new asphalt paving and striping on the existing parking lot, new landscaping, a new basketball court and patio at the laundry center along with new site furnishings.
</p>
                 
