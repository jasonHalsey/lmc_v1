<h1 class="ContactTitle">LMC gets invovled with REACH’s annual Paint &amp; Repair-a-thon</h1>

<div class="pressContainer" >
<p>Portland, OR - October14, 2009</p>
<p>Founded in 2004, Portland’s LMC Construction believes in helping their neighbors put down roots in places they can call home.</p>

<p><img src="../images/clyde_Volunteer.jpg" width="400" height="270" alt="Clyde Zahn" /><br />
<b>2009 PARAT recipient Mrs. Pierce with LMC volunteer Clyde Zahn</b></p>
 
<p>REACH has been the proud beneficiary of LMC’s generosity through founder and President Chris Duffin.  Sending a sizeable volunteer crew to REACH’s annual Paint &amp; Repair-a-thon for the first time this year, LMC has donated over $2,500 in cash, materials, and time. They also recently made a $1,000 matching sponsor gift for REACH’s fundraising efforts through the 2009 Willamette Week Give!Guide.</p>
<p>“There is a huge need for quality, affordable housing,” says LMC President Chris Duffin. “Partnering with REACH has been a long-time coming. It’s been fantastic, and we’re looking forward to completing our first project and getting more involved in the future.”</p>

<p>We are excited to continue partnering with LMC on projects like the Walnut Park Apartments rennovation in NE Portland and appreciate their generous support. Thanks, LMC!</p>
    <p>
	-REACH Community Development Newsletter</p>
