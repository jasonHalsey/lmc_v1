<h1 class="ContactTitle">LMC's Ellington Project is the Renovated Property of the Year!</h1>

<div class="pressContainer" >
<p>Portland, OR - May 21, 2009</p>
<p>The ACE Awards were held last night at the Oregon Convention Center, presented by the Metro Multifamily Housing Association. Metro Multifamily Housing Association (MMHA) represents residential property-managers and vendors throughout Portland, Southern Washington, and down through the Willamette Valley to Medford. The members manage over 150,000 units in these areas and represent every service related to the industry.</p>

<p>The Apartment Community Excellence Awards are a way to recognize and honor individuals who stand out as role-models, exemplary professionals, and community builders in the multifamily rental-housing industry; and who actively strive to elevate the industry as a whole.</p> 

<p>The ACE Awards honors nominees in categories such as Managers of the year, Assistant Managers of the year and Maintenance persons of the year, according to the sizes of the residential communities.  Additionally ACE recognizes awards for Leasing person of the year, Property of the year, Renovated property of the year, Civic Responsibility, Company Recognition Awards, and the Ross Dey Award for demonstrated excellence in the property management industry over a lifetime.</p>

<p>Nearly 1,000 attendees packed the room last night to honor the best of the best in our industry. This was an exciting night for the team and we are proud to announce that the award for renovated property of the year over 1 Million went to The Ellington Apartments. This is quite an accomplishment as we were recognized for the positive impact we have had on the property and the surrounding community. This renovation changed the face of this neighborhood and created a community where people want to live.</p>

<p>We proudly announced that this award was the icing on the cake as we reached our goal this month of 90% occupancy and are currently 95% leased. This future is bright for this property and we are confident we are set for a positive year ahead of rent growth and stabilized occupancy.</p>
